package com.example.demo.dto;

public class EstadisticasSistemaDTO {
    private long totalAlumnos;
    private long totalCursos;
    private long totalIncidencias;
    private long totalRegistros;
    private long totalPagos;
    private long totalReseñas;
    private long totalContenidos;
    private long totalEvaluaciones;

    // Getters y Setters
    public long getTotalAlumnos() { return totalAlumnos; }
    public void setTotalAlumnos(long totalAlumnos) { this.totalAlumnos = totalAlumnos; }

    public long getTotalCursos() { return totalCursos; }
    public void setTotalCursos(long totalCursos) { this.totalCursos = totalCursos; }

    public long getTotalIncidencias() { return totalIncidencias; }
    public void setTotalIncidencias(long totalIncidencias) { this.totalIncidencias = totalIncidencias; }

    public long getTotalRegistros() { return totalRegistros; }
    public void setTotalRegistros(long totalRegistros) { this.totalRegistros = totalRegistros; }

    public long getTotalPagos() { return totalPagos; }
    public void setTotalPagos(long totalPagos) { this.totalPagos = totalPagos; }

    public long getTotalReseñas() { return totalReseñas; }
    public void setTotalReseñas(long totalReseñas) { this.totalReseñas = totalReseñas; }

    public long getTotalContenidos() { return totalContenidos; }
    public void setTotalContenidos(long totalContenidos) { this.totalContenidos = totalContenidos; }

    public long getTotalEvaluaciones() { return totalEvaluaciones; }
    public void setTotalEvaluaciones(long totalEvaluaciones) { this.totalEvaluaciones = totalEvaluaciones; }
}
